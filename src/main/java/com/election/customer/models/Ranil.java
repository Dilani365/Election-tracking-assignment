package com.election.customer.models;

public class Ranil{
        private String name;
        private String party;
        private int age;
        private int politicsYears;


        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public String getParty() {
            return party;
        }

        public void setParty(String party) {
            this.party = party;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }

        public int getPoliticsYears() {
            return politicsYears;
        }

        public void setPoliticsYears(int politicsYears) {
            this.politicsYears = politicsYears;
        }


}
